package lib.kalu.permission.core.wrapper;

public class WrapperPermission {

    private boolean pass;
    private String name;

    public boolean isPass() {
        return pass;
    }

    public void setPass(boolean pass) {
        this.pass = pass;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
